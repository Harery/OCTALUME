"""Spec-kit-compatible Markdown templates.

Emits the file layout that github/spec-kit expects:

    <output-dir>/
    ├── memory/
    │   └── constitution.md
    └── specs/
        └── <feature-slug>/
            │── spec.md
            │── plan.md            (carries the OCTALUM 8-phase mapping)
            │── research.md
            │── data-model.md
            │── quickstart.md
            │── contracts/
            │   └── README.md
            └── tasks.md

All content is generated deterministically from the parsed ``Project``
model — no LLM required. See ``templates.py`` for the legacy
"octalum-classic" output format kept for backward compatibility.
"""

from __future__ import annotations

from datetime import date

from octalume.idea.parser import Project
from octalume.idea.templates import OCTALUME_PHASES, _bullets

# --- constitution -----------------------------------------------------------


def render_constitution(p: Project) -> str:
    return f"""# Project Constitution

> Governing principles for **{p.title}**.
> Seeded by `octalum idea` on {date.today().isoformat()}. Refine before locking.

These principles guide every downstream decision — `plan.md`, `tasks.md`,
and any code generated by spec-kit's `/speckit.implement` must respect them.

## I. Code Quality

- Code is read more than it is written. Optimise for the next reader.
- Every public function/endpoint has a docstring or schema entry.
- No commented-out code in `main`. Delete or branch.

## II. Testing Discipline

- A change without a test is a hypothesis, not a feature.
- Run the full suite locally before pushing; CI is a safety net, not a substitute.
- Tests describe behaviour, not implementation. Refactors should not break them.

## III. User Experience Consistency

- One pattern per concept. If two screens do "the same thing", they look the same.
- Errors are actionable: tell the user *what* went wrong and *what to do next*.
- Accessibility is not a Phase-5 task. WCAG 2.1 AA from day one.

## IV. Performance Awareness

- Measure before optimising; optimise the hot path, not the cold one.
- A page that takes > 1s to interact is broken.
- Database queries are explained (`EXPLAIN`) before merging.

## V. Security Posture

- Secrets never enter git. Use env vars + a secret manager.
- Dependencies are scanned in CI (Trivy / Snyk / `pip-audit`).
- Default-deny: every new endpoint is unauthenticated only by explicit choice.

## VI. Decision Hygiene

- Reversible decisions go fast. Irreversible decisions get a short ADR.
- Disagree and commit — but write down the disagreement.

---

_This constitution composes with the OCTALUM 8-phase SDLC framework and
github/spec-kit's `/speckit.constitution` command. Edit freely; this is
a starting point, not scripture._
"""


# --- spec ------------------------------------------------------------------


def render_spec(p: Project) -> str:
    features = _bullets(
        p.features[:20],
        empty="- _(parser detected no explicit features — fill in before /speckit.plan)_",
    )
    goals = _bullets(p.goals, empty="- _(no explicit goals detected — fill in)_")
    constraints = _bullets(p.constraints, empty="- _(none detected)_")

    # Synthesise user stories from the top features.
    user_stories = []
    for i, f in enumerate(p.features[:5], 1):
        user_stories.append(
            f"- **US-{i:02d}** — As a user, I want to *{f[:120]}* so that the product delivers its core value."
        )
    if not user_stories:
        user_stories.append(
            "- _(no user stories synthesisable — please add before /speckit.plan)_"
        )

    return f"""# Feature Spec — {p.title}

> **What & why** — functional requirements only. No tech choices here;
> those live in `plan.md`. Generated by `octalum idea` from a brain-dump
> on {date.today().isoformat()}.

## Summary

{p.summary or "_(no summary detected — please write a one-paragraph elevator pitch)_"}

## Primary domain

`{p.primary_domain}` (detected from keyword signals; override if wrong).

## Goals

{goals}

## User stories

{chr(10).join(user_stories)}

## Functional requirements

{features}

## Constraints

{constraints}

## Out of scope (v0.1)

- _List the things you explicitly will NOT build in the first slice._
- _A spec without an out-of-scope section silently scope-creeps._

## Acceptance criteria

- [ ] Every user story above has at least one end-to-end test.
- [ ] The "out of scope" list has been reviewed with stakeholders.
- [ ] Open questions in `research.md` are resolved or explicitly deferred.

---

_Next: `/speckit.plan` (or read the auto-generated `plan.md` already in this directory)._
"""


# --- plan ------------------------------------------------------------------


def render_plan(p: Project) -> str:
    stack = p.stack
    stack_rows = "\n".join(f"| {k} | {v} |" for k, v in stack.items())

    # OCTALUM 8-phase mapping inlined as a labelled section.
    phase_lines = []
    for name, desc in OCTALUME_PHASES:
        phase_lines.append(f"- **{name}** — {desc}")
    phases_md = "\n".join(phase_lines)

    return f"""# Implementation Plan — {p.title}

> **How** we will build what `spec.md` describes. Tech stack, architecture,
> and the cross-walk to the OCTALUM 8-phase SDLC framework.

## Tech stack (recommended)

| Concern | Choice |
|---------|--------|
{stack_rows}

_Rationale: optimised for time-to-first-deploy on `{p.primary_domain}` workloads.
Every entry is swappable — see `research.md` for the trade-offs._

## Architecture sketch

- **Entry point(s):** depends on domain — for `{p.primary_domain}`, see the tree below.
- **State / persistence:** see `data-model.md`.
- **External contracts:** see `contracts/` (API specs, event schemas).
- **Deployment target:** see the "hosting" / "ci" row in the stack table above.

## Suggested repo layout

See [`spec.md`](spec.md) "out of scope" before committing to this tree.
A starter layout for `{p.primary_domain}` projects:

```
{p.slug}/
│── src/
│── tests/
│── docs/
└── README.md
```

(For a fuller domain-specific tree, run `octalum idea quick … --target octalum-classic`
and consult the generated `STRUCTURE.md`.)

## OCTALUM 8-phase mapping

> The 8 phases below are preserved here so the OCTALUM methodology *composes*
> with spec-kit's `/speckit.tasks` and `/speckit.implement` commands — they
> don't conflict.

{phases_md}

`tasks.md` (in this same directory) is the spec-kit-native flattening of
phases 0–3; phases 4–7 are tracked at the OCTALUME workspace level. See
https://github.com/Harery/OCTALUME for the parent framework.

## Open questions blocking this plan

{_bullets(p.unknowns, empty="- _(none detected — that itself is suspicious; re-read the dump)_")}

---

_Next: refine with `/speckit.plan`, then generate `tasks.md` with `/speckit.tasks`._
"""


