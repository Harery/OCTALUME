# --- contracts -----------------------------------------------------------


def render_contracts_readme(p: Project) -> str:
    return f"""# Contracts — {p.title}

> External-facing contracts. Drop API specs, event schemas, and RPC
> definitions here. Spec-kit's `/speckit.implement` reads from this folder.

## Conventions

- One file per externally-visible surface.
- OpenAPI 3.1 → `api-spec.yaml` or `api-spec.json`.
- AsyncAPI 2.x → `events.yaml`.
- Proto / gRPC → `*.proto`.
- WebSocket / SignalR → `signalr-spec.md`.

## What lives here vs. inline

- **Lives here:** anything another team / system consumes.
- **Inline (in code):** internal-only DTOs, private RPCs.

## Starter

Replace this file with the appropriate spec for `{p.primary_domain}`:

- `saas` → `api-spec.yaml` (REST) + optional `events.yaml`
- `iot`  → MQTT topic catalog (`mqtt-topics.md`)
- `data` → `events.yaml` (Kafka/PubSub) + `dbt-contracts.yaml`
- `cli`  → `cli-contract.md` (flag/arg surface — yes, that's a contract too)
- `ml`   → `inference-api.yaml`
"""


# --- tasks ----------------------------------------------------------------


def render_tasks(p: Project) -> str:
    """Spec-kit-shaped flat task list with parallelism markers and deps."""
    lines = [
        f"# Tasks — {p.title}",
        "",
        "> Ordered, executable task list. `[P]` marks tasks that can run in",
        "> parallel with the previous one. Dependencies noted inline. Composes",
        "> with `/speckit.implement`.",
        "",
        "## Phase 0 — Discovery (US-* setup)",
        "",
        "- [ ] **T001** Validate the problem statement with 3+ potential users.",
        "- [ ] **T002** [P] Define success metrics (1 north-star + 2 supporting).",
        "- [ ] **T003** Resolve open questions in `research.md` (or explicitly defer).",
        "",
        "## Phase 1 — Architecture",
        "",
        "- [ ] **T010** Lock the tech stack in `plan.md` (after `/speckit.plan`).",
        "- [ ] **T011** [P] Draft `data-model.md` entities + relationships.",
        "- [ ] **T012** [P] Draft `contracts/` (API/event specs).",
        "",
        "## Phase 2 — Design & spike",
        "",
        "- [ ] **T020** Wireframe the primary user flow.",
        "- [ ] **T021** [P] Define the API contract end-to-end.",
        "- [ ] **T022** 1-day spike on the highest-risk feature (see `research.md`).",
        "",
        "## Phase 3 — Build (feature work)",
        "",
    ]
    if p.features:
        for i, f in enumerate(p.features[:8], start=30):
            tag = " [P]" if i > 30 else ""
            lines.append(f"- [ ] **T0{i:02d}**{tag} Implement: {f[:140]}")
    else:
        lines.append("- [ ] **T030** Implement vertical slice #1 (define the slice first).")
    lines += [
        "",
        "## Phase 4 — Integration & test",
        "",
        "- [ ] **T040** Contract tests for every entry in `contracts/`.",
        "- [ ] **T041** [P] End-to-end happy-path test for each US-* in `spec.md`.",
        "- [ ] **T042** [P] Dependency scan (Trivy / Snyk / pip-audit).",
        "",
        "## Phase 5 — Hardening",
        "",
        "- [ ] **T050** Threat-model (STRIDE).",
        "- [ ] **T051** [P] Confirm secrets never hit git (gitleaks in CI).",
        "- [ ] **T052** [P] Accessibility pass (WCAG 2.1 AA).",
        "",
        "## Phase 6 — Release",
        "",
        "- [ ] **T060** CI: build → test → deploy.",
        "- [ ] **T061** [P] Wire structured logs + traces.",
        "- [ ] **T062** Define rollback procedure.",
        "",
        "## Phase 7 — Operate",
        "",
        "- [ ] **T070** SLOs + error budget defined.",
        "- [ ] **T071** [P] Alerting wired (PagerDuty / Better Stack / equivalent).",
        "- [ ] **T072** Monthly roadmap review scheduled.",
        "",
        "---",
        "",
        "_Phase numbering follows the OCTALUM 8-phase SDLC framework so this file",
        "is readable as both a spec-kit `tasks.md` and an OCTALUM phase plan._",
        "",
    ]
    return "\n".join(lines)


# --- entry point -----------------------------------------------------------


def render_all_speckit(p: Project) -> dict[str, str]:
    """Return a {relative_path: content} map for the spec-kit layout."""
    slug = p.slug
    base = f"specs/{slug}"
    return {
        "memory/constitution.md": render_constitution(p),
        f"{base}/spec.md": render_spec(p),
        f"{base}/plan.md": render_plan(p),
        f"{base}/research.md": render_research(p),
        f"{base}/data-model.md": render_data_model(p),
        f"{base}/quickstart.md": render_quickstart(p),
        f"{base}/contracts/README.md": render_contracts_readme(p),
        f"{base}/tasks.md": render_tasks(p),
    }
