# --- data-model -----------------------------------------------------------


def render_data_model(p: Project) -> str:
    domain = p.primary_domain
    starter = ""
    if domain == "saas":
        starter = """\
| Entity | Fields | Notes |
|--------|--------|-------|
| `User` | `id`, `email`, `created_at`, `tenant_id` | Auth via Clerk/Auth.js. |
| `Tenant` | `id`, `name`, `plan`, `created_at` | One tenant per paying account. |
| `Subscription` | `id`, `tenant_id`, `stripe_subscription_id`, `status` | Stripe-mirrored. |
"""
    elif domain == "iot":
        starter = """\
| Entity | Fields | Notes |
|--------|--------|-------|
| `Device` | `id`, `serial`, `firmware_version`, `last_seen_at` | Pi/ESP32. |
| `Reading` | `device_id`, `ts`, `metric`, `value` | TimescaleDB hypertable. |
| `Alert` | `id`, `device_id`, `ts`, `rule_id`, `acknowledged` | MQTT-driven. |
"""
    elif domain == "data":
        starter = """\
| Entity | Fields | Notes |
|--------|--------|-------|
| `Source` | `id`, `connector`, `last_synced_at` | Airbyte/Fivetran-ish. |
| `RawTable` | `source_id`, `schema`, `table`, `rows` | Lands in bronze layer. |
| `MartTable` | `name`, `dbt_model`, `refreshed_at` | dbt-managed. |
"""
    else:
        starter = """\
| Entity | Fields | Notes |
|--------|--------|-------|
| _(define your core entities here)_ | | |
"""

    return f"""# Data Model — {p.title}

> Schema and core entities. Populate before `/speckit.implement`.

## Entities

{starter}

## Relationships

- _(Draw the foreign-key graph. Mermaid `erDiagram` works in GitHub-rendered MD.)_

```mermaid
erDiagram
    USER ||--o{{ SESSION : has
```

## Migrations / change discipline

- Schema changes ship with an explicit migration file.
- Backwards-incompatible columns get a 2-deploy cycle (add → backfill → switch reads → drop).
- Every table has `created_at` and (where mutable) `updated_at`.

## PII / sensitive fields

- Mark every column carrying PII.
- Encryption-at-rest is the default; encryption-in-use only where threat-model demands it.
- Document retention policy per table.
"""


